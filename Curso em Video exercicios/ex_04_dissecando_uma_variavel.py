alguma_coisa = input("Digite alguma coisa: ")


# Métodos String
print("O tipo primitivo é ", type(alguma_coisa))
print("Só tem espaço", alguma_coisa .isspace())
print("É um número? ", alguma_coisa.isnumeric())
print("É alfabético? ", alguma_coisa.isalpha())
print("É Alpha numérico?", alguma_coisa.isalnum())
print("E maiúscula?", alguma_coisa.isupper())
print("É minúsculo?", alguma_coisa.islower())
print("Está capitalizado?", alguma_coisa.istitle())